# Subida de documento
uploaded = st.file_uploader("ğŸ“„ Sube un PDF sobre acreditaciÃ³n", type=["pdf"])

if uploaded:
    try:
        import fitz
        doc = fitz.open(stream=uploaded.read(), filetype="pdf")
        text = ""
        for page in doc:
            text += page.get_text()
        doc.close()
        st.session_state.document_text = text[:5000]
        st.session_state.document_name = uploaded.name
        st.success(f"âœ… PDF procesado: {st.session_state.document_name}")
    except Exception as e:
        st.error(f"âŒ Error al procesar PDF: {str(e)[:100]}")

# Mostrar historial de chat
for message in st.session_state.messages:
    with st.chat_message(message["role"]):
        st.markdown(message["content"])

# Input del usuario (ÃšNICO lugar donde se modifica session_state.messages)
if prompt := st.chat_input("Escribe tu pregunta sobre acreditaciÃ³n..."):
    # Guardar mensaje del usuario
    st.session_state.messages.append({"role": "user", "content": prompt})
    
    with st.chat_message("user"):
        st.markdown(prompt)
    
    # Generar respuesta
    with st.chat_message("assistant"):
        placeholder = st.empty()
        placeholder.markdown("ğŸ§  Analizando...")
        
        context = st.session_state.document_text if st.session_state.document_text else "No hay documento cargado."
        
        try:
            stream = client.chat.completions.create(
                model=MODEL,
                messages=[
                    {"role": "system", "content": "Eres ChatAcredita, asistente de acreditaciÃ³n de la EISC. Responde SOLO con base en el documento proporcionado."},
                    {"role": "user", "content": f"Documento:\n{context}\n\nPregunta: {prompt}"}
                ],
                max_tokens=500,
                temperature=0.3,
                stream=True
            )
            
            answer = ""
            for chunk in stream:
                if chunk.choices[0].delta.content:
                    answer += chunk.choices[0].delta.content
                    placeholder.markdown(answer + "â–Œ")
            
            placeholder.markdown(answer)
            st.session_state.messages.append({"role": "assistant", "content": answer})
            
        except Exception as e:
            error_msg = f"âŒ Error: {str(e)[:150]}"
            placeholder.markdown(error_msg)
            st.session_state.messages.append({"role": "assistant", "content": error_msg})

# Mensaje de bienvenida si no hay historial
